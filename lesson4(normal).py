# Задание-1:
# Вывести символы в нижнем регистре, которые находятся вокруг
# 1 или более символов в верхнем регистре.
# Т.е. из строки "mtMmEZUOmcq" нужно получить ['mt', 'm', 'mcq']
# Решить задачу двумя способами: с помощью re и без.


def search_down_symbols_re(line):
    records = re.findall(r'[A-Z]*[a-z]+[A-Z]+', line)
    results = [re.search(r'[a-z]+', str(record)).group(0) for record in records]
    print(results)


def search_down_symbols(line):
    start_position = 0
    end_position = 0
    results = []
    for letter in line:
        if letter.istitle():
            letters = line[start_position:end_position]
            if letters != '':
                results.append(letters)
            start_position = end_position + 1
            end_position += 1
        else:
            end_position += 1
    print(results)


# Задание-2:
# Вывести символы в верхнем регистре, слева от которых находятся
# два символа в нижнем регистре, а справа - два символа в верхнем регистре.
# Т.е. из строки
# "GAMkgAYEOmHBSQsSUHKvSfbmxULaysmNOGIPHpEMujalpPLNzRWXfwHQqwksrFeipEUlTLec"
# нужно получить список строк: ['AY', 'NOGI', 'P']
# Решить задачу двумя способами: с помощью re и без.


def search_upper_symbols_re(line):
    records = re.findall(r'[a-z]{2}[A-Z]+[A-Z]{2}', line)
    results = [str(record[2:len(record)-2]) for record in records]
    print(results)


def search_upper_symbols(line):
    start_position = 0
    end_position = 0
    low_count = 2
    low_counter = 0
    upper_count = 3
    upper_counter = 0
    results = []
    for letter in line:
        if not letter.istitle():  # Если буква маленькая
            if low_counter >= low_count and upper_counter >= upper_count:
                results.append(line[start_position:end_position - 2])
                low_counter = 0
            end_position += 1
            start_position = end_position
            if upper_counter > 0:
                low_counter = 0
            if low_counter <= low_count:
                low_counter += 1
            upper_counter = 0
        elif letter.istitle():  # Если буква большая
            if low_counter < low_count:
                end_position += 1
                start_position = end_position
            elif low_counter >= low_count:
                end_position += 1
            upper_counter += 1
    print(results)


